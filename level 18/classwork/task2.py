# მომხმარებლის მიერ შეყვანილი ქულა
score = float(input("შეიყვანეთ თქვენი გამოცდის ქულა: "))

if score > 50:
    print("გამოცდა ჩააბარა")
    
    # შემოწმება, არის თუ არა ქულა ლუწი ან კენტი
    if score % 2 == 0:
        print("კარგი ბიჭიხარ")
    else:
        print("ტორტიხარ")
else:
    print("გამოცდა ვერ ჩაიბარა")
